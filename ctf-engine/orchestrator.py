"""
orchestrator.py

The Orchestrator manages the full generation pipeline:
1. Spin up sandbox
2. Run ReAct agent (multi-phase: research → build → deploy)
3. Validate with Strix (white-box quick scan)
4. Register the lab
"""

import uuid
import logging
import json
import time
from pathlib import Path

from config import (
    MAX_RETRIES, WORKSPACE_DIR,
    STRIX_ENABLED, STRIX_TIMEOUT,
)
from tools.register_lab import RegisterLabTool
from sandbox import DockerSandbox
from agent import ReActAgent
from validators.strix_validator import StrixValidator


logger = logging.getLogger(__name__)


class Orchestrator:

    def __init__(self):
        self.register_tool = RegisterLabTool()
        self.strix_validator = StrixValidator()

    def generate_lab(self, spec: dict, user_id: str = "anonymous") -> dict:
        """
        Main entry point. Receives spec from frontend.
        Spins up sandbox, runs agent, validates with Strix, registers.
        """
        if "lab_id" not in spec:
            spec["lab_id"] = f"lab_{uuid.uuid4().hex[:8]}"

        lab_id = spec["lab_id"]
        logger.info(f"[Orchestrator] Starting generation for lab {lab_id}")
        logger.info(f"[Orchestrator] Spec: {spec.get('vuln_type')} / {spec.get('difficulty')}")

        start_time = time.time()
        sandbox = DockerSandbox()

        try:
            # ── 1. Start Sandbox ────────────────────────────────────────
            sandbox.start(lab_id)

            # ── 2. Run ReAct Agent ──────────────────────────────────────
            workspace_path = str(sandbox.host_workspace_dir)
            assigned_port = sandbox.assigned_port
            spec["assigned_port"] = assigned_port  # Pass port to agent
            agent = ReActAgent(spec=spec, sandbox=sandbox, workspace=workspace_path)
            result = agent.run()

            if result.get("status") != "success":
                logger.error(f"[Orchestrator] Agent failed: {result.get('reason', result.get('error'))}")
                return {
                    "status": "failed",
                    "lab_id": lab_id,
                    "error_history": [f"Agent failed: {result.get('reason', result.get('error'))}"],
                    "message": "Lab generation failed during autonomous agent loop.",
                    "iterations": result.get("iterations", 0),
                    "phase": result.get("phase", "unknown"),
                }

            elapsed_gen = time.time() - start_time
            logger.info(f"[Orchestrator] Agent completed in {elapsed_gen:.0f}s, {result.get('iterations', '?')} iterations")

            # ── 3. Create metadata ──────────────────────────────────────
            meta_dir = sandbox.host_workspace_dir / "meta"
            meta_dir.mkdir(parents=True, exist_ok=True)

            challenge_data = {
                "title": spec.get("title", f"{spec.get('vuln_type', 'Challenge')} Lab"),
                "description": spec.get("description", "Generated by VulnForge CTF Engine"),
                "category": spec.get("category", "web"),
                "difficulty": spec.get("difficulty", "medium"),
                "flag": spec.get("flag", "CTF{flag_not_found}"),
                "vuln_type": spec.get("vuln_type", "unknown"),
                "ports": {"web_service": 3000},
            }
            (meta_dir / "challenge.json").write_text(json.dumps(challenge_data, indent=2))

            # ── 4. Strix Validation (white-box quick scan) ──────────────
            validation_result = None
            validation_status = "skipped"

            if STRIX_ENABLED:
                logger.info("[Orchestrator] 🔍 Running Strix white-box validation...")
                try:
                    target_url = f"http://localhost:{assigned_port}"
                    validation_result = self.strix_validator.validate(
                        lab_dir=workspace_path,
                        target_url=target_url,
                        vuln_type=spec.get("vuln_type", "unknown"),
                        expected_flag=spec.get("flag", ""),
                    )
                    validation_status = "confirmed" if validation_result.passed else "failed"
                    logger.info(
                        f"[Orchestrator] Strix validation: {validation_status} "
                        f"(method={validation_result.method}, flag_found={validation_result.flag_found})"
                    )
                    if validation_result.details:
                        logger.info(f"[Orchestrator] Strix details: {validation_result.details}")

                    # Save validation report
                    validation_report = {
                        "status": validation_status,
                        "passed": validation_result.passed,
                        "method": validation_result.method,
                        "flag_found": validation_result.flag_found,
                        "details": validation_result.details,
                        "unintended_vulns": validation_result.unintended_vulns,
                    }
                    (meta_dir / "validation.json").write_text(
                        json.dumps(validation_report, indent=2)
                    )

                except Exception as e:
                    logger.warning(f"[Orchestrator] Strix validation error (non-blocking): {e}")
                    validation_status = "error"
            else:
                logger.info("[Orchestrator] Strix validation disabled — skipping")

            par_time = round(time.time() - start_time, 2)
            service_urls = {"app": f"http://localhost:{assigned_port}"}

            # ── 5. Register Lab ─────────────────────────────────────────
            logger.info("[Orchestrator] Registering lab...")
            try:
                reg_result = self.register_tool.register(
                    lab_dir=str(sandbox.host_workspace_dir),
                    service_urls=service_urls,
                    par_time=par_time,
                    user_id=user_id,
                )
            except Exception as e:
                logger.warning(f"[Orchestrator] Registration failed (non-blocking): {e}")
                reg_result = {
                    "session_token": "none",
                    "expires_at": "none",
                    "challenge_title": challenge_data["title"],
                    "challenge_description": challenge_data["description"],
                }

            logger.info(f"[Orchestrator] ✅ Lab {lab_id} ready in {par_time}s!")

            return {
                "status": "success",
                "lab_id": lab_id,
                "service_urls": service_urls,
                "par_time": par_time,
                "iterations": result.get("iterations", 0),
                "session_token": reg_result.get("session_token"),
                "expires_at": reg_result.get("expires_at"),
                "challenge_title": reg_result.get("challenge_title"),
                "challenge_description": reg_result.get("challenge_description"),
                "validation": validation_status,
                "validation_details": (
                    validation_result.details if validation_result else "Validation skipped"
                ),
                "message": (
                    f"Lab ready! Target: {service_urls.get('app')} | "
                    f"Time: {par_time}s | Iterations: {result.get('iterations', '?')} | "
                    f"Validation: {validation_status}"
                ),
            }

        except Exception as e:
            logger.error(f"[Orchestrator] Unhandled error: {e}", exc_info=True)
            return {
                "status": "failed",
                "lab_id": lab_id,
                "error_history": [str(e)],
                "message": f"Lab generation failed: {e}",
            }

        finally:
            # Keep sandbox running so the lab containers stay up
            # Only clean up the sandbox container, not the workspace
            pass
